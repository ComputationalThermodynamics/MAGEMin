/* Select required thermodynamic database */
EM_db_sb Access_SB_EM_DB(int id, int EM_dataset) {
	EM_db_sb Entry_EM;

	if (EM_dataset == 2011){	
	 	Entry_EM = arr_em_db_sb_sb11[id]; 
	}
    else if (EM_dataset == 2021){	
        Entry_EM = arr_em_db_sb_sb21[id]; 
   }
   else if (EM_dataset == 2024){	
        Entry_EM = arr_em_db_sb_sb24[id]; 
   }
	else{
		printf(" Wrong endmember dataset, values should be 2011, 2021 or 2024\n");
		printf(" -> using default 2011 to avoid ugly crash\n");
		Entry_EM = arr_em_db_sb_sb11[id]; 
	}
	
	return Entry_EM;
}
